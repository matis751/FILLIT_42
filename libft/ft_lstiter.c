#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list *iter;

	if (!lst || !f)
		return ;
	iter = lst;
	while (iter->next)
	{
		f(iter);
		iter = iter->next;
	}
	f(iter);
}
